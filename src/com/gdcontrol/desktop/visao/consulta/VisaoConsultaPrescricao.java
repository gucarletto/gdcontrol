    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        if(!edPesquisa.getText().isEmpty()){
            tableModelPrescricao.limpar();
            String pesquisa = (String) cbCampoPesquisa.getSelectedItem();
            switch(pesquisa){
                case OPCAO_ID:
                    try{
                        filtraId(Integer.parseInt(edPesquisa.getText()));
                    }catch(NumberFormatException e){}
                    break;
                case OPCAO_DATA_INICIO:
                    filtraDataInicio(edPesquisa.getText());
                    break;
                case OPCAO_DATA_FIM:
                    filtraDataInicio(edPesquisa.getText());
                    break;
                case OPCAO_NOME_MEDICO:
                    filtraDataInicio(edPesquisa.getText());
                    break;
                case OPCAO_NOME_MEDICACAO:
                    filtraDataInicio(edPesquisa.getText());
                    break;
            }
            tableModelPrescricao.fireTableDataChanged();
        }else{
            carregaPrescricoes();
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

