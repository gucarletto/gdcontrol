    PrescricaoTableModel tableModelPrescricao = new PrescricaoTableModel();
    
    private void carregaPrescricoes() {
        tableModelPrescricao.limpar();
        for (Prescricao prescricao : (List<Prescricao>) getControle().listar()) {
            tableModelPrescricao.addPrescricao(prescricao);
        }
        tableModelPrescricao.fireTableDataChanged();
    }
    
    private void filtraId(int id){
        Prescricao prescricao = getControle().filtraId(id);
        tableModelPrescricao.addPrescricao(prescricao);
    }
    
    private void filtraDataInicio(String dataInicio){
        for (Prescricao prescricao : (List<Prescricao>) getControle().filtraDataInicio(dataInicio)) {
            tableModelPrescricao.addPrescricao(prescricao);
        }
    }
    
    private void filtraDataFim(String dataFim){
        for (Prescricao prescricao : (List<Prescricao>) getControle().filtraDataFim(dataFim)) {
            tableModelPrescricao.addPrescricao(prescricao);
        }
    }
    
    private void filtraNomeMedico(String nome){
        for (Prescricao prescricao : (List<Prescricao>) getControle().filtraNomeMedico(nome)) {
            tableModelPrescricao.addPrescricao(prescricao);
        }
    }
    
    private void filtraNomeComercialMedicacao(String nome){
        for (Prescricao prescricao : (List<Prescricao>) getControle().filtraNomeComercialMedicacao(nome)) {
            tableModelPrescricao.addPrescricao(prescricao);
        }
    }
    
    @Override
    public void getOpcoesPesquisa() {
        Vector comboBoxItems = new Vector();
        comboBoxItems.add(OPCAO_ID);
        comboBoxItems.add(OPCAO_DATA_INICIO);
        comboBoxItems.add(OPCAO_DATA_FIM);
        comboBoxItems.add(OPCAO_NOME_MEDICO);
        comboBoxItems.add(OPCAO_NOME_MEDICACAO);
        final DefaultComboBoxModel model = new DefaultComboBoxModel(comboBoxItems);
        this.cbCampoPesquisa.setModel(model);
    }

    @Override
    public ControleConsultaPrescricao getControle() {
        return this.controle;
    }
