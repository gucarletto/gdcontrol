    private void carregaTiposTeste() {
        tableModelTipoTeste.limpar();
        for (TipoTeste tip : (List<TipoTeste>) getControle().listar()) {
            tableModelTipoTeste.addTipoTeste(tip);
        }
        tableModelTipoTeste.fireTableDataChanged();
    }

