    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        if (!edPesquisa.getText().isEmpty()) {
            tableModelAlimento.limpar();
            String pesquisa = (String) cbCampoPesquisa.getSelectedItem();
            switch (pesquisa) {
                case OPCAO_ID:
                    try {
                        filtraId(Integer.parseInt(edPesquisa.getText()));
                    } catch (NumberFormatException e) {
                    }
                    break;
                case OPCAO_NOME:
                    filtraNome(edPesquisa.getText());
                    break;
                case OPCAO_ID_GRUPO:
                    try {
                        filtraIdGrupo(Integer.parseInt(edPesquisa.getText()));
                    } catch (NumberFormatException e) {
                    }
                    break;
                case OPCAO_DESCRICAO_GRUPO:
                    filtraDescricaoGrupo(edPesquisa.getText());
                    break;
            }
            tableModelAlimento.fireTableDataChanged();
        } else {
            carregaAlimentos();
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

