    @Override
    public void getOpcoesPesquisa() {
        Vector comboBoxItems = new Vector();
        comboBoxItems.add(OPCAO_ID);
        comboBoxItems.add(OPCAO_NOME);
        comboBoxItems.add(OPCAO_ID_GRUPO);
        comboBoxItems.add(OPCAO_DESCRICAO_GRUPO);
        final DefaultComboBoxModel model = new DefaultComboBoxModel(comboBoxItems);
        this.cbCampoPesquisa.setModel(model);
    }

    private void carregaAlimentos() {
        tableModelAlimento.limpar();
        for (Alimento ali : (List<Alimento>) getControle().listar()) {
            tableModelAlimento.addAlimento(ali);
        }
        tableModelAlimento.fireTableDataChanged();
    }

    private void filtraId(int id) {
        Alimento grp = getControle().filtraId(id);
        if (grp != null) {
            tableModelAlimento.addAlimento(grp);
        }
    }

    private void filtraNome(String nome) {
        for (Alimento ali : (List<Alimento>) getControle().filtraNome(nome)) {
            tableModelAlimento.addAlimento(ali);
        }
    }

    private void filtraIdGrupo(int id) {
        for (Alimento ali : (List<Alimento>) getControle().filtraIdGrupo(id)) {
            tableModelAlimento.addAlimento(ali);
        }
    }

    private void filtraDescricaoGrupo(String descricao) {
        for (Alimento ali : (List<Alimento>) getControle().filtraDescricaoGrupo(descricao)) {
            tableModelAlimento.addAlimento(ali);
        }
    }

    @Override
    public ControleConsultaAlimento getControle() {
        return this.controle;
    }
