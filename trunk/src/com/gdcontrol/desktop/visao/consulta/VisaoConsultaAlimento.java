    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int indice = tbAlimento.getSelectedRow();
        if (indice >= 0) {
            int opcao = JOptionPane.showConfirmDialog(this, "Confirma exclusão do Alimento selecionado?");
            if(opcao == JOptionPane.YES_OPTION){
                getControle().excluir(tableModelAlimento.getAlimento(indice));
                carregaAlimentos();
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        int indice = tbAlimento.getSelectedRow();
        if (indice >= 0) {
            VisaoManutencaoAlimento manutencao = new VisaoManutencaoAlimento(null, true);
            ControleManutencaoAlimento controleManutencao = (ControleManutencaoAlimento) manutencao.getControle();
            controleManutencao.setModelo(tableModelAlimento.getAlimento(indice));
            controleManutencao.setTela(manutencao);
            controleManutencao.carregaTela();
            manutencao.setVisible(true);
            carregaAlimentos();
        }
    }//GEN-LAST:event_btAlterarActionPerformed

